// CVC Exchange client (search.cvc.edu).
//
// CVC Exchange (a Quottly app) is the statewide California Community Colleges
// online course exchange. It is NOT login-blocked, renders results as plain
// server-side HTML, and covers ~115 CA community colleges — which makes it an
// excellent (a) real-data source for colleges whose own schedule we can't crack
// and (b) ground-truth cross-check for the colleges we *do* scrape.
//
// IMPORTANT — what CVC does and does not have:
//   - It lists ONLINE / async / sync-online courses only (delivery_methods=Online).
//     It is an *online exchange*, so it never shows a college's in-person sections.
//     => Treat CVC as authoritative for "what online courses exist", not for the
//        full catalog. A college can be legitimately live with 2000 in-person
//        courses and only a handful on CVC.
//   - It is keyword-driven: there is no "list everything" endpoint. To enumerate a
//     college's online catalog we sweep a broad set of subject keywords and dedupe
//     by the CVC course id.
//
// Search model (verified against the live site):
//   GET https://search.cvc.edu/search
//     filter[target_school_ids][]=<cvcId>   // restrict to courses TAUGHT by this college
//     filter[subject]=<keyword>             // required — keyword over subject/title/code
//     filter[search_type]=open_search
//     filter[search_all_universities]=true
//     commit=Find Classes
//     page=<n>                              // 10 results/page
//   Results are `.course` cards; total shown as "<N> courses".
