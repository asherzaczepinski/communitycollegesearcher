export function hasCvc(slug) {
  return Object.prototype.hasOwnProperty.call(CVC_IDS, slug);
}

function searchUrl({ cvcId, subject, page }) {
  const p = new URLSearchParams();
  p.append('filter[target_school_ids][]', String(cvcId));
  p.append('filter[subject]', subject);
  p.append('filter[search_type]', 'open_search');
  p.append('filter[search_all_universities]', 'true');
  p.append('commit', 'Find Classes');
  if (page > 1) p.append('page', String(page));
  return `${BASE}?${p.toString()}`;
}

// Parse one results page's `.course` cards into normalized course objects.
// Skips the JS-loaded "home college" grouping card (it has no real course rows).
function parsePage(html, sourceUrl) {
  const $ = cheerio.load(html);
  const courses = [];
  $('.course').each((_, el) => {
    const $c = $(el);
    const link = $c.find('a.course-details-link').first();
    const linkText = link.text().replace(/\s+/g, ' ').trim();
    if (!linkText) return; // grouping/placeholder card — no course link
    // "BIOL105 - General Biology" -> code + title
    let code = null, title = linkText;
    const m = linkText.match(/^([A-Z]{2,}[\s-]?\d[\w.\/-]*)\s*[-–—]\s*(.+)$/);
    if (m) { code = m[1].replace(/\s+/g, ''); title = m[2].trim(); }

    const college = $c.find('.course-head .font-semibold').first().text().replace(/\s+/g, ' ').trim();
    const units = ($c.find('.credit p').first().text().match(/[\d.]+/) || [null])[0];
    const term = $c.find('.term p').first().text().replace(/\s+/g, ' ').trim() || null;
    // Transferability designations (IGETC / Cal-GETC / CSU BREADTH — the GE
    // requirement areas a course satisfies).
    const transfer = $c.find('.req span').map((_, s) => $(s).text().trim()).get().filter(Boolean);
    const tuitionText = ($c.find('span.text-c_link').first().text().match(/\$[\d,.]+/) || [null])[0];
    const tuition = tuitionText ? Number(tuitionText.replace(/[^\d.]/g, '')) : null;
    // Course badges live in .tags-container; read each <li> label.
    const badges = $c.find('.tags-container .tag-container').map((_, b) => $(b).text().replace(/\s+/g, ' ').trim()).get();
    const zeroTextbookCost = badges.some((b) => /zero textbook/i.test(b));
    const qualityReviewed = badges.some((b) => /quality reviewed/i.test(b));
    // Split the "Mon DD - Mon DD" term into start/end date strings when present.
    const dm = (term || '').match(/^([A-Z][a-z]{2}\s+\d{1,2})\s*[-–]\s*([A-Z][a-z]{2}\s+\d{1,2})$/);
    const href = link.attr('href') || '';
    const cvcId = (href.match(/\/courses\/(\d+)/) || [null, null])[1];

    // Everything CVC exposes per card, kept as structured metadata so the UI and
    // Supabase can filter on transferability / tuition / badges — not buried in a
    // description string.
    const meta = {
      transferable: transfer,            // ['IGETC','Cal-GETC','CSU BREADTH']
      igetc: transfer.includes('IGETC'),
      calGetc: transfer.includes('Cal-GETC'),
      csuBreadth: transfer.some((t) => /CSU/i.test(t)),
      tuition,                            // numeric dollars
      zeroTextbookCost,
      qualityReviewed,
      startDate: dm ? dm[1] : null,
      endDate: dm ? dm[2] : null,
      cvcCourseId: cvcId,
      cvcUrl: href ? new URL(href, BASE).href : null,
      // Specific GE / A–G areas this course satisfies (from the cached area map).
      geAreas: cvcId ? geAreasFor(cvcId) : null,
    };

    courses.push({
      code,
      title,
      modality: 'online', // CVC is an online exchange; every result is online
      term,
      units,
      instructor: null, // not on the card (would need the detail page)
      section: cvcId, // CVC course id — stable per-section identifier
      description: transfer.length ? `Transfers: ${transfer.join(', ')}` : null,
      url: href ? new URL(href, BASE).href : sourceUrl,
      meta,
      _college: college,
      _transfer: transfer,
      _tuition: tuitionText,
    });
  });
  // total count is rendered as "<N> courses"
  const totalMatch = $('body').text().match(/([\d,]+)\s+courses/);
  const total = totalMatch ? Number(totalMatch[1].replace(/,/g, '')) : null;
  return { courses, total };
}

// Fetch one course's detail page (/courses/<id>) and extract everything the
// section listing exposes — EXCEPT available seats (intentionally skipped). The
// section fields each render once per section, in the same order, so we zip the
// parallel arrays by index.
export async function fetchCourseDetail(cvcCourseId) {
  const res = await fetchText(`${HOST}/courses/${cvcCourseId}`, { timeoutMs: 25000, retries: 2 });
  if (!res.ok) return null;
  const $ = cheerio.load(res.body);
  const txt = (el) => $(el).text().replace(/\s+/g, ' ').trim();

  const description = txt($('.course-description').first()) || null;
  const crn = $('.section-details-crn').map((_, e) => txt(e)).get();
  const prof = $('.section-details-professor').map((_, e) => txt(e)).get();
  const date = $('.section-details-date').map((_, e) => txt(e)).get();
  const notes = $('.section-details-notes').map((_, e) => txt(e)).get();
  // Format ("Online - Asynchronous"/"...Synchronous") renders as a button per section.
  const fmt = $('button').filter((_, e) => /Online\s*-\s*(A?synchronous)/i.test(txt(e)))
    .map((_, e) => txt(e)).get();
  const normFmt = (f) => (/asynchronous/i.test(f) ? 'Asynchronous' : /synchronous/i.test(f) ? 'Synchronous' : f || null);

  const n = Math.max(crn.length, prof.length, date.length, fmt.length);
  const sections = [];
  for (let i = 0; i < n; i++) {
    sections.push({
      crn: crn[i] || null,
      dates: date[i] || null,
      professor: prof[i] && prof[i] !== 'TBA' ? prof[i] : null,
      format: normFmt(fmt[i] || ''),
      notes: notes[i] || null,
    });
  }
  const professors = [...new Set(sections.map((s) => s.professor).filter(Boolean))];
  const formats = [...new Set(sections.map((s) => s.format).filter(Boolean))];
  // Prerequisite / recommended-prep text is embedded in the description.
  const prereq = (description && (description.match(/(Prerequisite[s]?:[^.]*\.)/i) || description.match(/(Strongly recommended:[^.]*\.)/i)) || [])[1] || null;
  return { description, sections, professors, formats, prerequisites: prereq };
}

// Run async fn over items with a small concurrency cap. Returns results in order.
async function mapLimit(items, limit, fn) {
  const out = new Array(items.length);
  let i = 0;
  async function worker() {
    while (i < items.length) {
      const idx = i++;
      try { out[idx] = await fn(items[idx], idx); } catch { out[idx] = null; }
    }
  }
  await Promise.all(Array.from({ length: Math.min(limit, items.length) }, worker));
  return out;
}

// Fetch all online courses CVC lists for one college (by slug), sweeping the
// subject keywords and deduping by CVC course id. Returns [] if the college is
// not on CVC. Never throws on a single bad fetch — it logs and continues.
//
// With { withDetails:true } it also fetches each course's detail page and merges
// section-level data (instructor, sync/async format, section dates/CRNs/notes,
// full description, prerequisites) into the course — everything but seat counts.
export async function fetchCvcCourses(slug, {
  subjects = SUBJECTS,
  maxPagesPerSubject = 25,
  onProgress = null,
  withDetails = false,
  detailConcurrency = 6,
} = {}) {
  const cvcId = CVC_IDS[slug];
  if (!cvcId) return [];

  const byId = new Map(); // cvc course id -> course (dedupe across subjects)
  for (const subject of subjects) {
    for (let page = 1; page <= maxPagesPerSubject; page++) {
      const url = searchUrl({ cvcId, subject, page });
      const res = await fetchText(url, { timeoutMs: 25000, retries: 2 });
      if (!res.ok) break; // give up this subject, move on
      const { courses } = parsePage(res.body, url);
      if (!courses.length) break; // past the last page for this subject
      for (const c of courses) {
        const key = c.section || `${c.code}|${c.title}|${c.term}`;
        if (!byId.has(key)) byId.set(key, c);
      }
      if (onProgress) onProgress({ slug, subject, page, found: byId.size });
      if (courses.length < 10) break; // last page (fewer than a full page)
    }
  }
  const list = [...byId.values()];

  // Optional deep pass: enrich each course with section-level detail.
  if (withDetails) {
    let done = 0;
    await mapLimit(list, detailConcurrency, async (c) => {
      const id = c.meta?.cvcCourseId || c.section;
      if (!id) return;
      const d = await fetchCourseDetail(id);
      if (d) {
        if (d.description) c.description = d.description; // real catalog description
        if (d.professors.length) c.instructor = d.professors.join(', ');
        c.meta = {
          ...c.meta,
          formats: d.formats,                 // ['Asynchronous'] / ['Synchronous'] / both
          professors: d.professors,
          prerequisites: d.prerequisites,
          sections: d.sections,               // [{crn,dates,professor,format,notes}] — no seats
          sectionCount: d.sections.length,
        };
      }
      done++;
      if (onProgress) onProgress({ slug, phase: 'details', found: done, total: list.length });
    });
  }
  return list;
}

// Just the headline number for a college: how many online courses CVC shows,
// using a single cheap subject probe is not enough, so this does a quick total
// via one broad query. Used by the validator for a fast "is there data?" check.
export async function cvcCourseTotal(slug, subject = 'a') {
  const cvcId = CVC_IDS[slug];
  if (!cvcId) return null;
  const res = await fetchText(searchUrl({ cvcId, subject, page: 1 }), { timeoutMs: 25000, retries: 2 });
  if (!res.ok) return null;
  return parsePage(res.body, res.url).total;
}
