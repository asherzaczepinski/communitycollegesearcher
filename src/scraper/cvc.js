// Broad subject keyword sweep. CVC has no "list all" endpoint, so we union the
// results of many keyword searches and dedupe by CVC course id. This list covers
// the common transferable disciplines; a college's online catalog is almost
// entirely reachable through it. Order is roughly most- to least-common.
